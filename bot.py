def main() -> None:
    if not BOT_TOKEN:
        raise RuntimeError("Set BOT_TOKEN in .env")

    asyncio.set_event_loop(asyncio.new_event_loop())

    app = Application.builder().token(BOT_TOKEN).build()
    conversation = ConversationHandler(
        entry_points=[CommandHandler("start", start)],
        states={
            ASK_NAME: [
                CallbackQueryHandler(meet, pattern="^meet$"),
                MessageHandler(filters.TEXT & ~filters.COMMAND, ask_age),
            ],
            ASK_AGE: [MessageHandler(filters.TEXT & ~filters.COMMAND, ask_work)],
            ASK_WORK: [MessageHandler(filters.TEXT & ~filters.COMMAND, ask_request)],
            ASK_REQUEST: [CallbackQueryHandler(handle_request, pattern="^request:")],
            ASK_REQUEST_OTHER: [MessageHandler(filters.TEXT & ~filters.COMMAND, handle_request_other)],
            ASK_INSTAGRAM: [MessageHandler(filters.TEXT & ~filters.COMMAND, intro_test)],
            WAIT_START: [CallbackQueryHandler(start_quiz, pattern="^start_quiz$")],
            QUIZ: [CallbackQueryHandler(handle_answer, pattern="^answer:")],
        },
        fallbacks=[CommandHandler("cancel", cancel), CommandHandler("start", start)],
    )
    app.add_handler(conversation)
    app.run_polling(allowed_updates=Update.ALL_TYPES)


if __name__ == "__main__":
    main()
