async def save_submission(update: Update, context: ContextTypes.DEFAULT_TYPE) -> None:
    DATA_DIR.mkdir(exist_ok=True)
    user = update.effective_user
    scores = context.user_data["scores"]
    max_score = max(scores.values())
    winners = [letter for letter, score in scores.items() if score == max_score]
    payload = {
        "created_at": datetime.now(timezone.utc).isoformat(),
        "telegram_user_id": user.id if user else None,
        "telegram_username": user.username if user else None,
        "name": context.user_data.get("name"),
        "age": context.user_data.get("age"),
        "work": context.user_data.get("work"),
        "request": context.user_data.get("request"),
        "instagram": context.user_data.get("instagram"),
        "answers": context.user_data.get("answers"),
        "scores": scores,
        "result_types": [TYPE_NAMES[letter] for letter in winners],
    }
    with SUBMISSIONS_FILE.open("a", encoding="utf-8") as file:
        file.write(json.dumps(payload, ensure_ascii=False) + "\n")


