async def start_quiz(update: Update, context: ContextTypes.DEFAULT_TYPE) -> int:
    query = update.callback_query
    await query.answer()
    context.user_data["answers"] = []
    context.user_data["scores"] = {"A": 0, "B": 0, "C": 0, "D": 0, "E": 0}
    await send_question(query.message, context)
    return QUIZ


async def send_question(message, context: ContextTypes.DEFAULT_TYPE) -> None:
    index = len(context.user_data["answers"])
    question, answers = QUESTIONS[index]
    answer_lines = [f"{letter}. {answer}" for letter, answer in zip("ABCDE", answers)]
    text = f"{question}\n\n" + "\n\n".join(answer_lines)
    rows = [[(f"Варіант {letter}", f"answer:{letter}")] for letter in "ABCDE"]
    await message.reply_text(text, reply_markup=keyboard(rows))


async def handle_answer(update: Update, context: ContextTypes.DEFAULT_TYPE) -> int:
    query = update.callback_query
    await query.answer()
    letter = query.data.removeprefix("answer:")
    context.user_data["answers"].append(letter)
    context.user_data["scores"][letter] += 1

    if len(context.user_data["answers"]) < len(QUESTIONS):
        await send_question(query.message, context)
        return QUIZ

    await query.message.reply_text("підраховуємо результати….")
    await asyncio.sleep(18)
    await send_results(query.message, context)
    await save_submission(update, context)
    await notify_admin(update, context)
    return ConversationHandler.END


